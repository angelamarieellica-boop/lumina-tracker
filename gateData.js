/**
 * Returns gate data for a given gate number.
 * @param {number} gateNumber
 * @returns {object|null}
 */
function getGate(gateNumber) {
  return gateData[gateNumber] || null;
}

/**
 * Returns all of Angela's natal gates (isNatal: true).
 */
function getNatalGates() {
  return Object.entries(gateData)
    .filter(([, g]) => g.isNatal)
    .map(([num, g]) => ({ number: parseInt(num), ...g }));
}

/**
 * Formats a gate's wisdom for display in the daily digest.
 * @param {number} gateNumber
 * @returns {string}
 */
function formatGateWisdom(gateNumber) {
  const gate = gateData[gateNumber];
  if (!gate) return '';
  const natal = gate.isNatal ? ' ✦ (Natal Gate)' : '';
  return `Gate ${gateNumber}: ${gate.name}${natal}
Shadow → Gift → Siddhi: ${gate.shadow} → ${gate.gift} → ${gate.siddhi}
${gate.theme}
${gate.isNatal ? gate.reflectorNote : ''}`.trim();
}

/**
 * Returns a compact prompt-ready string for AI synthesis.
 * @param {number} gateNumber
 * @returns {string}
 */
function gateForAIPrompt(gateNumber) {
  const gate = gateData[gateNumber];
  if (!gate) return '';
  const natal = gate.isNatal ? ' [Angela\'s natal gate]' : '';
  return `Gate ${gateNumber} — ${gate.name}${natal}: Shadow: ${gate.shadow} | Gift: ${gate.gift} | Siddhi: ${gate.siddhi}. Theme: ${gate.theme}${gate.isNatal ? ' Reflector note: ' + gate.reflectorNote : ''}`;
}

